    private void btn_logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logoutActionPerformed
        // TODO add your handling code here:
        int pesan=JOptionPane.showConfirmDialog(null, "Yakin Ingin Keluar?","",JOptionPane.YES_NO_OPTION
            ,JOptionPane.QUESTION_MESSAGE);
        if(pesan==JOptionPane.YES_OPTION){
            Form_Login l = new Form_Login();
            if(getTxt_statusair().getText().equalsIgnoreCase("Tersedia")){
                l.getKeterangan().setText("Tersedia");
            }
            else
            {
                l.getKeterangan().setText("Tidak tersedia");
            }
            l.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btn_logoutActionPerformed

