    private void btn_statusairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_statusairActionPerformed
        // TODO add your handling code here:
        int pesan=JOptionPane.showConfirmDialog(null, "Air Tersedia?","",JOptionPane.YES_NO_OPTION
            ,JOptionPane.QUESTION_MESSAGE);
        if(pesan==JOptionPane.YES_OPTION){
            a=0;
            getTxt_statusair().setText("Tersedia");
            btn_input.setEnabled(true);
            getTxt_statusair().setEditable(false);
        }
        else if (pesan==JOptionPane.NO_OPTION)
        {
            a=1;
            getTxt_statusair().setText("Tidak Tersedia");
            btn_input.setEnabled(false);
            getTxt_statusair().setEditable(false);
        }
    }//GEN-LAST:event_btn_statusairActionPerformed

